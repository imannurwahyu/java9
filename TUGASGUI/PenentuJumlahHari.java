    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
       String tahunStr = tahun.getText(); 
    String bulan = (String) jComboBox1.getSelectedItem(); 
    String hasil = Output.getText(); 

   
    String lokasiFile = "E:/Jawa/hasil_perhitungan.txt"; // 
    // String lokasiFile = "E:/Jawa/hasil_perhitungan.txt";

    try {
        BufferedWriter writer = new BufferedWriter(new FileWriter(lokasiFile));
        writer.write("Tahun: " + tahunStr);
        writer.newLine();
        writer.write("Bulan: " + bulan);
        writer.newLine();
        writer.write("Hasil: " + hasil);
        writer.close();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan di " + lokasiFile);
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan file.");
        e.printStackTrace();
    }
    }//GEN-LAST:event_simpanActionPerformed

